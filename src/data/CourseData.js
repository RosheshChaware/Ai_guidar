// Provide helper to get all grouped courses dynamically
export const getCategorizedCourses = () => {
  const categories = [
    "Engineering", 
    "Science", 
    "Medical", 
    "Computer / IT", 
    "Commerce", 
    "Arts / Humanities", 
    "Law", 
    "Creative Fields"
  ];
  
  const grouped = {};
  categories.forEach(cat => grouped[cat] = []);
  
  COURSE_PATHWAYS.forEach(course => {
     if (grouped[course.category]) {
         grouped[course.category].push(course);
     } else {
         grouped["Engineering"].push(course);
     }
  });

  // Sort within each category by course name A-Z
  Object.keys(grouped).forEach(cat => {
     grouped[cat].sort((a, b) => a.courseName.localeCompare(b.courseName));
  });

  return grouped;
};
